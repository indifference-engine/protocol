#include <stdint.h>
#include "../../helpers/main.h"
#include "../../../src/read_u32s.h"

void test()
{
  uint32_t actual_u32s[] = {0x71510A04, 0x62FA97A2, 0xE017D981};
  const uint8_t actual_buffer[] = {0x08, 0xe5, 0x2b, 0x39, 0xcb, 0x1b, 0x85, 0x13, 0x31, 0x89, 0x10, 0x0F, 0x11, 0x87, 0xAE, 0xF0, 0x88, 0xED};

  assert(read_u32s(actual_buffer, 18, actual_u32s, 3, 5), "read_u32s valid");

  const uint32_t expected_u32s[] = {0x3113851b, 0x110F1089, 0x88F0AE87};
  assert_u32s(expected_u32s, actual_u32s, 3, "read_u32s valid u32s");
  const uint8_t expected_buffer[] = {0x08, 0xe5, 0x2b, 0x39, 0xcb, 0x1b, 0x85, 0x13, 0x31, 0x89, 0x10, 0x0F, 0x11, 0x87, 0xAE, 0xF0, 0x88, 0xED};
  assert_u8s(expected_buffer, actual_buffer, 18, "read_u32s valid buffer");
}
