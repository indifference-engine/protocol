#include <stdint.h>
#include "../../helpers/main.h"
#include "../../../src/write_u64s.h"

void test()
{
  const uint64_t actual_u64s[] = {0xbae1637f5593ca37, 0xc6d4fd26872746bc, 0xc30b62d8b376401d};
  uint8_t actual_buffer[] = {0x08, 0xe5, 0x2b, 0x39, 0xcb, 0x1b, 0xf1, 0x87, 0x31, 0x09, 0xfe, 0x76, 0x70, 0xa6, 0xdf, 51, 0x8b, 0x00, 0x88, 0xa9, 0x72, 0x7c, 0x73, 0xc7, 0xf6};

  assert(!write_u64s(actual_u64s, 3, 2, actual_buffer, 25), "write_u64s invalid_1");

  const uint64_t expected_u64s[] = {0xbae1637f5593ca37, 0xc6d4fd26872746bc, 0xc30b62d8b376401d};
  assert_u64s(expected_u64s, actual_u64s, 3, "write_u64s invalid_1 u64s");
  const uint8_t expected_buffer[] = {0x08, 0xe5, 0x2b, 0x39, 0xcb, 0x1b, 0xf1, 0x87, 0x31, 0x09, 0xfe, 0x76, 0x70, 0xa6, 0xdf, 51, 0x8b, 0x00, 0x88, 0xa9, 0x72, 0x7c, 0x73, 0xc7, 0xf6};
  assert_u8s(expected_buffer, actual_buffer, 25, "write_u64s invalid_1 buffer");
}
